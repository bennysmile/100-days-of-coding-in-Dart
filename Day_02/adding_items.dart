void main() {
  var products = ['Shampoo', 'Conditioner', 'Leave-In'];
  products.add('Hair Oil'); // Adding a new product
  products.addAll(['Hair Mask', 'Hair Serum']); // Adding multiple products
  print(products);
}
// This program demonstrates how to add items to a list in Dart.
// It adds both single and multiple items to the list and prints the updated list.
// The `add` method adds a single item, while `addAll` adds multiple items at once.
