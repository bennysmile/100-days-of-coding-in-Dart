void main() {
  int age = 25; // Integer variable explicitly defined
  double height = 5.9; // Double variable explicitly defined
  String name = "Bernard"; // String variable explicitly defined
  bool isStudent = true; // Boolean variable explicitly defined

  print("Name: $name"); // Using string interpolation to print the name
  print("Age: $age"); // Using string interpolation to print the age
  print("Height: $height"); // Using string interpolation to print the height
  print(
      "Is Student: $isStudent"); // Using string interpolation to print the student status
}

// This program demonstrates the use of variables and data types in Dart.
// It defines variables for age, height, name, and student status, and prints them to the console.
