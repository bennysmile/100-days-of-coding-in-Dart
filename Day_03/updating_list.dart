void main() {
  var products = ['Shampoo', 'Conditioner', 'Leave-In'];
  products[0] = 'Hair Shampoo'; // Updating the first element
  products[1] = 'Hair Conditioner'; // Updating the second element
  products[2] = 'Hair Leave-In'; // Updating the third element
  print(products);
}

// This program demonstrates how to update elements in a list in Dart.
// It modifies each product in the list and prints the updated list.
