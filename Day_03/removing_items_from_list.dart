void main() {
  var products = ['Shampoo', 'Conditioner', 'Leave-In'];
  products.remove('Conditioner'); // Removing a specific product
  products.removeAt(0); // Removing the first product
  print(products);
}

// This program demonstrates how to remove items from a list in Dart.
// It removes both a specific item and an item at a specific index, then prints the updated list.
